public class OperacionesAritmeticas {
    
    /**
    * @param a es el primer sumando de la suma
    * @param b es el segundo sumando de la suma
    * @return el método devuelve la suma de a y b
    */
    public static double op1(double a, double b) {
        return a + b;
    }
   /**
    * @param a es el primer término de la resta
    * @param b es el segundo término de la resta
    * @return el método devuelve la diferencia entre a y b
    */
    public static double op2(double a, double b) {
        return a - b;
    }
}
